package exceptions;

public class EmptyTreeException extends Exception {

	public EmptyTreeException() {
		super();
	}

	public EmptyTreeException(String arg0) {
		super(arg0);
	}

	public EmptyTreeException(Throwable arg0) {
		super(arg0);
	}

	public EmptyTreeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public EmptyTreeException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
